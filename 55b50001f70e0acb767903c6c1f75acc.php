	<script>
 function openGalleryModal(imageSrc, title, price) {
    document.getElementById('galleryImage').src = imageSrc;
    document.getElementById('galleryTitle').innerText = title;
    document.getElementById('galleryPrice').innerText = price;
    document.getElementById('confirmationMessage').classList.add('d-none');
    // إعادة تعيين التقييم والقياس إذا أردت
    resetRating();
    resetSizeButtons();
    // عرض المودال (باستخدام Bootstrap 5)
    var myModal = new bootstrap.Modal(document.getElementById('galleryModal'));
    myModal.show();
  }

  // اختيار المقاس
  function selectSize(button) {
    resetSizeButtons();
    button.classList.add('btn-primary');
    button.classList.remove('btn-outline-secondary');
    // يمكن حفظ المقاس المختار في متغير إذا أردت استخدامه لاحقاً
  }

  function resetSizeButtons() {
    document.querySelectorAll('#galleryModal .btn-outline-secondary, #galleryModal .btn-primary').forEach(btn => {
      btn.classList.remove('btn-primary');
      btn.classList.add('btn-outline-secondary');
    });
  }

  // تقييم النجوم
  function rateProduct(rating) {
    const stars = document.querySelectorAll('#galleryRating .star');
    stars.forEach((star, index) => {
      star.textContent = index < rating ? '★' : '☆';
    });

  }

  function resetRating() {
    document.querySelectorAll('#galleryRating .star').forEach(star => star.textContent = '☆');
  }


  function confirmGalleryCart() {

    const confirmation = document.getElementById('confirmationMessage');
    confirmation.textContent = 'The dress has been successfully added to the cart!';

    confirmation.classList.remove('d-none');
  }
</script>



